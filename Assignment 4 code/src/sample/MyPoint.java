package sample;

public class MyPoint {
    // Variables
    private double x, y; // variables for point p(x,y)

    // Methods
    public double getX() { return x; } // get the value of x
    public double getY() { return y; } // get the value of y

    // Constructors
    public MyPoint (double x, double y){ // set the x and y given values of object point p
        this.x = x; // set x
        this.y = y; // set y
    }
}
